public class Convert {
// THIS FILE WILL HANDLE CONVERSIONS.
    public static double usd2Peso(double dollar) {
        return dollar * 20.47;
    }

    public static double usd2Euro(double dollar) {
        return dollar * 0.97;
    }

    public static double usd2Yen(double dollar) {
        return dollar * 156.51;
    }

    public static double usd2Pound(double dollar) {
        return dollar * 0.82;
    }
}
